# Faça um programa que leia uma temperatura em graus Celsius e apresente-a convertida em graus Fahrenheit.
# A fórmula de conversão é: F = (9 * C + 160) / 5, na qual F é a temperatura em Fahrenheit e C é a temperatura em Celsius;


# Entrada dos Dados
celcius = float(input('Informe a temperadura em celsius: '))
Fahrenheit = (9*celcius+160)/5
# Processamento de Dados
print(f'A temperatura em Fahrenheit é : {Fahrenheit} ')