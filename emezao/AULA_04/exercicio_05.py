# Construa um programa que calcule o fatorial de um numero inteiro fornecido pelo usuário.

# Entrada dos Dados
n = int(input("digite um numero inteiro: "))


# Processamento dos Dados
fatorial = 1 
for i in range(1, n + 1 ):
    fatorial = 1 


# Saída de Dados 
print(f"O fatorial de {n} é: {fatorial}")